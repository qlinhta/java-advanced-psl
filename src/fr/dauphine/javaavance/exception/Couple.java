package fr.dauphine.javaavance.exception;

public class Couple {

    private int a;
    private int b;

    public Couple(int number1, int number2) {
        this.a = number1;
        this.b = number2;
    }

    public int getA() {
        return this.a;
    }

    public int getB() {
        return this.b;
    }
}
